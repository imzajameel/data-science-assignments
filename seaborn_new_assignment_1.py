#Question.2 Load the "fmri" dataset using the load_dataset function of seaborn. Plot a line plot using x =
#"timepoint" and y = "signal" for different events and regions.
#Answer.2 :
import seaborn as sns
import matplotlib.pyplot as plt

# Load the "fmri" dataset
fmri_data = sns.load_dataset("fmri")

# Create a line plot using Seaborn
plt.figure(figsize=(10, 6))
sns.lineplot(data=fmri_data, x="timepoint", y="signal", hue="event", style="region", markers=True)

# Add title and labels
plt.title("Line Plot of Signal Over Time by Event and Region")
plt.xlabel("Timepoint")
plt.ylabel("Signal")

# Show legend
plt.legend(title="Event")

# Display the plot
plt.show()

#Question.3 Load the "titanic" dataset using the load_dataset function of seaborn. Plot two box plots using x =
#'pclass', y = 'age' and y = 'fare'.
#Note: pclass, age, and fare are columns in the titanic dataset.
#Answer.3 :
import seaborn as sns
import matplotlib.pyplot as plt

# Load the "titanic" dataset
titanic_data = sns.load_dataset("titanic")

# Create two box plots using Seaborn
plt.figure(figsize=(12, 6))

# Box plot for 'age' by 'pclass'
plt.subplot(1, 2, 1)
sns.boxplot(data=titanic_data, x='pclass', y='age')
plt.title("Box Plot of Age by Pclass")
plt.xlabel("Pclass")
plt.ylabel("Age")

# Box plot for 'fare' by 'pclass'
plt.subplot(1, 2, 2)
sns.boxplot(data=titanic_data, x='pclass', y='fare')
plt.title("Box Plot of Fare by Pclass")
plt.xlabel("Pclass")
plt.ylabel("Fare")

# Adjust layout and display the plots
plt.tight_layout()
plt.show()

