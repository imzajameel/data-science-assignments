#Question.6 Use the "flights" dataset from seaborn to plot a heatmap.
#Answer.6 :
import seaborn as sns
import matplotlib.pyplot as plt

# Load the "flights" dataset
flights_data = sns.load_dataset("flights")

# Pivot the data to create a matrix suitable for a heatmap
flights_pivot = flights_data.pivot_table(index='month', columns='year', values='passengers')

# Create a heatmap using Seaborn
plt.figure(figsize=(10, 6))
sns.heatmap(data=flights_pivot, cmap='YlGnBu', annot=True, fmt="d", linewidths=.5)

# Add title and labels
plt.title("Flights Heatmap")
plt.xlabel("Year")
plt.ylabel("Month")

# Display the plot
plt.show()